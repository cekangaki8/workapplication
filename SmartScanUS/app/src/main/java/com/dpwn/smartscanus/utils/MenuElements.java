public class MenuElements {
    /** Id for the image to be used. */
    private int imageResourceId;
    /** Text to displayed for the header. */
    private String menuHeaderText;
    /** Details to be displayed for the menu item. */
    private String menuDetailText;

    /** Id for the fragment to be loaded. */
    private String nextFragmentId;

    /* Constructor must pass all three properties. */
    public MenuElements(int imageResourceId, String menuHeaderText, String menuHeaderDetails, String nextFragmentId) {
        this.imageResourceId = imageResourceId;
        this.menuHeaderText = menuHeaderText;
        this.menuDetailText = menuHeaderDetails;
        this.nextFragmentId = nextFragmentId;
    }

    /** Getter for the header text. */
    public String getMenuHeaderText() {
        return menuHeaderText;
    }

    /** Setter for the header text. */
    public void setMenuHeaderText(String menuHeaderText) {
        this.menuHeaderText = menuHeaderText;
    }

    /** Getter for the detail text. */
    public String getMenuDetailText() {
        return menuDetailText;
    }

    /** Setter for the detail text. */
    public void setMenuDetailText(String menuDetailText) {
        this.menuDetailText = menuDetailText;
    }

    /** Getter for the image id. */
    public int getImageResourceId() {
        return imageResourceId;
    }

    /** Setter for the image id. */
    public void setImageResourceId(int imageResourceId) {
        this.imageResourceId = imageResourceId;
    }

    /** Getter for the next Fragment Identifier. */
    public String getNextFragmentId() {
        return nextFragmentId;
    }

    /** Setter for the next Fragment Identifier. */
    public void setNextFragmentId(String nextFragmentId) {
        this.nextFragmentId = nextFragmentId;
    }
}

